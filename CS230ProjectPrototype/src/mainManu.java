    private String[] addToArray(String[] arrin, String stringin)
    {
        String[] arrout = new String[arrin.length + 1];
        
        for (int i = 0; i < arrin.length; i++){
            arrout[i] = arrin[i];
        }
        
        arrout[arrin.length] = stringin;
        return arrout;
    }
    
